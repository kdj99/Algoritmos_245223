package Problema1;

/**
 * Programa que imprime los numeros de 1 al 1000 mediante
 * un ciclo for
 * @author Chuck
 */
public class Problema1 {

    public static void main(String[] args) {
        int x = 0;
        System.out.println("Impresion de numeros del 1 al 100");
        for (int j = 0; j < 100; j++) {
            x = x + 1;
            System.out.println(x);
        }
    }
}
