package Problema_23;

/**
 * Programa que imprime los numeros del 0 a 100 pero separando
 * las progresiones en columnnas y filas.
 * @author Chuck
 */
public class Problema_23 {
    public static void main(String[] args) {
        int x;
        System.out.println("Impresion de numeros del 1 al 100");
        for (x = 0; x <= 9; x++) {
            System.out.printf("%-20d",x);
        }
        System.out.println("\t");
        for (x = 10; x <=19 ; x++) {
            System.out.printf("%-20d",x);
        }
        System.out.println("\t");
        for (x = 20; x <=29 ; x++) {
            System.out.printf("%-20d",x);
        }
        System.out.println("\t");
        for (x = 30; x <=39 ; x++) {
            System.out.printf("%-20d",x);
        }
        System.out.println("\t");
        for (x = 40; x <=49 ; x++) {
            System.out.printf("%-20d",x);
        }
        System.out.println("\t");
        for (x = 50; x <=59 ; x++) {
            System.out.printf("%-20d",x);
        }
        System.out.println("\t");
        for (x = 60; x <=69 ; x++) {
            System.out.printf("%-20d",x);
        }
        System.out.println("\t");
        for (x = 70; x <=79 ; x++) {
            System.out.printf("%-20d",x);
        }
        System.out.println("\t");
        for (x = 80; x <=89 ; x++) {
            System.out.printf("%-20d",x);
        }
        System.out.println("\t");
        for (x = 90; x <=100 ; x++) {
            System.out.printf("%-20d",x);
        }
    }
    }

